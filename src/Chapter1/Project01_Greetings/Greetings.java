package Chapter1.Project01_Greetings;

/**
 * The Greetings class is a simple program designed to display three messages
 * to the console. It serves as an introductory example of Java programming
 * to demonstrate how to output text using the System.out.println method.
 *
 * This program prints the following messages:
 * 1. Welcome to Java
 * 2. Welcome to Computer Science
 * 3. Programming is fun
 */
public class Greetings {
    /**
     * The main method serves as the entry point for the program execution.
     * It displays three messages to the console using the System.out.println method.
     *
     * @param args Command-line arguments passed to the program during execution (not used in this implementation).
     */
    public static void main(String[] args) {
        System.out.println("Welcome to Java");
        System.out.println("Welcome to Computer Science");
        System.out.println("Programming is fun");
    }
}
